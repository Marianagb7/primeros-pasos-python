"""
Mostrar la tabla del 5 empleando primeroel while y seguidamente de
nuevo empleando el for.
(Definir un comentario previo a cada algoritmo y un comentaro de bloque al principio
del programa)
"""
print("Tabla del 5")
#algoritmo para mostrar la tabla de multiplicar del 5 con while
x=5
while x<=50:
    print(x)
    x=x+5
print("Tabla de multiplicar del 5")
#algoritmo con el for
for x in range(5,51,5):
    print(x)
