num1 = int(input("Ingresa el primer número: "))
num2 = int(input("ingresa el segundo numero: "))

if num1 > num2:
    addition = num1 + num2
    subtraction = num1 - num2
    print("La suma de los dos número es: ", addition, "la diferencia es: ", subtraction)
else:
    multiplication = num1 * num2
    division = num1 / num2
    print(" El producto de los dos número es: ", multiplication, "el resultado de la division es :", division)
    
